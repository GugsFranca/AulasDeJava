package fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		
		double a = 4.3;
		System.out.println(a);
		
		//A palavra var descobre qual é a variavel, isso é uma inferencia
		var b = 4.5; 
		System.out.println(b);
		
		var c = "LOL negro";
		System.out.println(c);
	}
}
