package streaming.desafio;

public class Cavalo {
    final String nome;
    final int alturaDeSalto;
    final boolean saudavel;
    final boolean temCavaleiro;

    public Cavalo (String nome, int alturaDeSalto, boolean saudavel, boolean temCavaleiro){
        this.nome = nome;
        this.alturaDeSalto = alturaDeSalto;
        this.saudavel = saudavel;
        this.temCavaleiro = temCavaleiro;
    }
}
